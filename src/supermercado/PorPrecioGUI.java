    private void clearTable() {

        DefaultTableModel precioTableModel = (DefaultTableModel) precioTable.getModel();

        int filas = precioTable.getRowCount() - 1;

        for (int i = filas; i >= 0; i--) {
            precioTableModel.removeRow(i);
        }

    }

