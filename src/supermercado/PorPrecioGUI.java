    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed

        DefaultTableModel precioTableModel = (DefaultTableModel) precioTable.getModel();

        clearTable();

        try {
            float minNum = Float.parseFloat(minPriceField.getText());
            float maxNum = Float.parseFloat(maxPriceField.getText());

            for (Producto producto : MenuGUI.listaProductos) {
                if (producto.getPrecio() > minNum && producto.getPrecio() < maxNum) {
                    precioTableModel.addRow(new Object[]{
                        producto.getCodigo(), producto.getDescripcion(),
                        "$ " + producto.getPrecio(), producto.getStock()});
                }
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El valor ingresado es incorrecto",
                    "ERROR", JOptionPane.WARNING_MESSAGE);
            minPriceField.setText("Min");
            maxPriceField.setText("Max");
        }


    }//GEN-LAST:event_searchButtonActionPerformed

