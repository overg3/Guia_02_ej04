    private void clearTable() {

        DefaultTableModel nTablaModel = (DefaultTableModel) nTabla.getModel();

        int filas = nTabla.getRowCount() - 1;

        for (int i = filas; i >= 0; i--) {
            nTablaModel.removeRow(i);
        }

    }

