    private void nFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nFieldKeyReleased

        DefaultTableModel nTablaModel = (DefaultTableModel) nTabla.getModel();

        clearTable();

        for (Producto producto : MenuGUI.listaProductos) {

            String search = nField.getText().toLowerCase(); //variable creada para comparación insensible a Mayus/Min

            if (producto.getDescripcion().toLowerCase().startsWith(search)) {
                nTablaModel.addRow(new Object[]{producto.getCodigo(),
                    producto.getDescripcion(), "$ " + producto.getPrecio(),
                    producto.getStock()
                });
            }
        }

    }//GEN-LAST:event_nFieldKeyReleased


